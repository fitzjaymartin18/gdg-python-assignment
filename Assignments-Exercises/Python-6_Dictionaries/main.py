# Creating a dictionary
my_dict = {"name": "Fitz", "age": 20}
print("Orginal dictionary:", my_dict)

# Adding an item to dictionary
my_dict["city"] = "Quezon City"
print("Dictionary after adding an item:", my_dict)

# Updating an item in dictionary
my_dict["age"] = 21
print("Dictionary after updating an item:", my_dict)

# Removing an item from dictionary
del my_dict["city"]
print("Dictionary after removing an item:", my_dict)